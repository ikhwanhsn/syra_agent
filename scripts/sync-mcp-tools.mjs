/**
 * Generate MCP tool catalog from api/config/agentTools.js (single source of truth).
 * Run: node scripts/sync-mcp-tools.mjs
 */
import { writeFileSync, mkdirSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { AGENT_TOOLS } from '../api/config/agentTools.js';
import { resolvePillarForToolId } from '../api/config/pillars.js';
import { X402_DISCOVERY_RESOURCE_PATHS } from '../api/config/x402DiscoveryResourcePaths.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const outPath = path.join(repoRoot, 'mcp-server/src/generated/toolCatalog.ts');

/** Express `:id` → OpenAPI-style `{id}` for path templates */
function toTemplatePath(apiPath) {
  return String(apiPath || '').replace(/:([^/]+)/g, '{$1}');
}

function sanitizeToolNamePart(value) {
  return String(value || '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '')
    .slice(0, 64);
}

function resolveAccess(tool) {
  if (tool.zerionPath) return 'upstream';
  if (tool.agentDirect) return 'agent-direct';
  return 'http';
}

/** High-value + discovery + pillar facade tool ids always in curated profile */
const CURATED_TOOL_IDS = new Set([
  'health',
  'news',
  'signal',
  'sentiment',
  'spcx-intelligence',
  'equity-intelligence',
  'brain',
  'analytics-summary',
  'arbitrage-experiment',
  'jupiter-quote',
  'pumpfun-trending',
  'pumpfun-movers',
  'pumpfun-analyzer',
  'pumpfun-scout',
  'rise-scout',
  'coingecko-scout',
  '8004-stats',
  '8004-leaderboard',
  '8004-agents-search',
  'nansen-smart-money-netflow',
  'web-search',
  'crawl',
  'browser-use',
  'jupiter-swap-order',
  'squid-route',
  'squid-status',
  'giza-protocols',
  'giza-agent',
  'bankr-balances',
  'bankr-prompt',
  'neynar-user',
  'siwa-nonce',
]);

for (const segment of X402_DISCOVERY_RESOURCE_PATHS) {
  const slug = segment.replace(/\//g, '-').replace(/^-+/, '');
  for (const tool of AGENT_TOOLS) {
    const toolPath = tool.path?.replace(/^\//, '') || '';
    if (toolPath === segment || toolPath.startsWith(`${segment}/`)) {
      CURATED_TOOL_IDS.add(tool.id);
    }
  }
}

/** @type {Array<Record<string, unknown>>} */
const entries = [];

for (const tool of AGENT_TOOLS) {
  if (tool.tempoPublic || tool.tempoPayout) continue;
  if (tool.path?.includes('__tempo_public__')) continue;

  const pillar = resolvePillarForToolId(tool.id, tool);
  const access = resolveAccess(tool);
  const idPart = sanitizeToolNamePart(tool.id);
  const toolName = `syra_${pillar}_${idPart}`;

  /** @type {Record<string, unknown>} */
  const entry = {
    toolName,
    toolId: tool.id,
    pillar,
    access,
    method: (tool.method || 'GET').toUpperCase(),
    name: tool.name,
    description: tool.description,
    priceUsd: tool.priceUsd,
    curated: CURATED_TOOL_IDS.has(tool.id),
  };

  if (access === 'http') {
    entry.path = toTemplatePath(tool.path);
  }

  if (tool.path?.includes('{') || tool.path?.includes(':')) {
    entry.pathParams = [...String(tool.path).matchAll(/:([^/]+)/g)].map((m) => m[1]);
  }

  entries.push(entry);
}

entries.sort((a, b) => String(a.toolName).localeCompare(String(b.toolName)));

const fileBody = `/**
 * AUTO-GENERATED by scripts/sync-mcp-tools.mjs — do not edit manually.
 * Source: api/config/agentTools.js
 * Regenerate: npm run sync:mcp-tools (from mcp-server/) or node scripts/sync-mcp-tools.mjs
 */

export type McpToolAccess = "http" | "agent-direct" | "upstream";

export type McpToolCatalogEntry = {
  toolName: string;
  toolId: string;
  pillar: string;
  access: McpToolAccess;
  method: string;
  path?: string;
  pathParams?: string[];
  name: string;
  description: string;
  priceUsd: number;
  curated: boolean;
};

export const MCP_TOOL_CATALOG: McpToolCatalogEntry[] = ${JSON.stringify(entries, null, 2)} as McpToolCatalogEntry[];

export const MCP_CURATED_TOOL_COUNT = MCP_TOOL_CATALOG.filter((t) => t.curated).length;
export const MCP_FULL_TOOL_COUNT = MCP_TOOL_CATALOG.length;
`;

mkdirSync(path.dirname(outPath), { recursive: true });
writeFileSync(outPath, fileBody, 'utf8');
console.log(
  `Wrote ${entries.length} MCP tools (${entries.filter((e) => e.curated).length} curated) → ${outPath}`,
);
