/**
 * Generate MCP tool catalog from api/config/agentTools.js (single source of truth).
 * Also regenerates curated-tool markdown tables between <!-- BEGIN|END:CURATED_MCP_TOOLS --> markers.
 * Run: node scripts/sync-mcp-tools.mjs
 */
import { writeFileSync, mkdirSync, readFileSync, existsSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { AGENT_TOOLS } from '../api/config/agentTools.js';
import { resolvePillarForToolId } from '../api/config/pillars.js';
import { X402_DISCOVERY_RESOURCE_PATHS } from '../api/config/x402DiscoveryResourcePaths.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const outPath = path.join(repoRoot, 'mcp-server/src/generated/toolCatalog.ts');
const curatedMdPath = path.join(repoRoot, 'mcp-server/docs/generated/curated-tools.md');
const BEGIN_MARKER = '<!-- BEGIN:CURATED_MCP_TOOLS -->';
const END_MARKER = '<!-- END:CURATED_MCP_TOOLS -->';

/** Files that embed the curated tools table between markers */
const MARKER_TARGETS = [
  path.join(repoRoot, 'web/public/skills.md'),
  path.join(repoRoot, 'mcp-server/README.md'),
];

/** Express `:id` → OpenAPI-style `{id}` for path templates */
function toTemplatePath(apiPath) {
  return String(apiPath || '').replace(/:([^/]+)/g, '{$1}');
}

function sanitizeToolNamePart(value) {
  return String(value || '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '')
    .slice(0, 64);
}

function resolveAccess(tool) {
  if (tool.zerionPath) return 'upstream';
  if (tool.agentDirect) return 'agent-direct';
  return 'http';
}

/** High-value + discovery + pillar facade tool ids always in curated profile */
const CURATED_TOOL_IDS = new Set([
  'health',
  'news',
  'signal',
  'sentiment',
  'spcx-intelligence',
  'equity-intelligence',
  'brain',
  'analytics-summary',
  'arbitrage-experiment',
  'jupiter-quote',
  'pumpfun-trending',
  'pumpfun-movers',
  'pumpfun-analyzer',
  'pumpfun-scout',
  'rise-scout',
  'coingecko-scout',
  '8004-stats',
  '8004-leaderboard',
  '8004-agents-search',
  'nansen-smart-money-netflow',
  'web-search',
  'crawl',
  'browser-use',
  'jupiter-swap-order',
  'squid-route',
  'squid-status',
  'giza-protocols',
  'giza-agent',
  'bankr-balances',
  'bankr-prompt',
  'neynar-user',
  'siwa-nonce',
]);

for (const segment of X402_DISCOVERY_RESOURCE_PATHS) {
  const slug = segment.replace(/\//g, '-').replace(/^-+/, '');
  for (const tool of AGENT_TOOLS) {
    const toolPath = tool.path?.replace(/^\//, '') || '';
    if (toolPath === segment || toolPath.startsWith(`${segment}/`)) {
      CURATED_TOOL_IDS.add(tool.id);
    }
  }
}

/** @type {Array<Record<string, unknown>>} */
const entries = [];

for (const tool of AGENT_TOOLS) {
  if (tool.tempoPublic || tool.tempoPayout) continue;
  if (tool.path?.includes('__tempo_public__')) continue;

  const pillar = resolvePillarForToolId(tool.id, tool);
  const access = resolveAccess(tool);
  const idPart = sanitizeToolNamePart(tool.id);
  const toolName = `syra_${pillar}_${idPart}`;

  /** @type {Record<string, unknown>} */
  const entry = {
    toolName,
    toolId: tool.id,
    pillar,
    access,
    method: (tool.method || 'GET').toUpperCase(),
    name: tool.name,
    description: tool.description,
    priceUsd: tool.priceUsd,
    curated: CURATED_TOOL_IDS.has(tool.id),
  };

  if (access === 'http') {
    entry.path = toTemplatePath(tool.path);
  }

  if (tool.path?.includes('{') || tool.path?.includes(':')) {
    entry.pathParams = [...String(tool.path).matchAll(/:([^/]+)/g)].map((m) => m[1]);
  }

  entries.push(entry);
}

entries.sort((a, b) => String(a.toolName).localeCompare(String(b.toolName)));

const curated = entries.filter((e) => e.curated);
const curatedCount = curated.length;
const fullCount = entries.length;

const fileBody = `/**
 * AUTO-GENERATED by scripts/sync-mcp-tools.mjs — do not edit manually.
 * Source: api/config/agentTools.js
 * Regenerate: npm run sync:mcp-tools (from mcp-server/) or node scripts/sync-mcp-tools.mjs
 */

export type McpToolAccess = "http" | "agent-direct" | "upstream";

export type McpToolCatalogEntry = {
  toolName: string;
  toolId: string;
  pillar: string;
  access: McpToolAccess;
  method: string;
  path?: string;
  pathParams?: string[];
  name: string;
  description: string;
  priceUsd: number;
  curated: boolean;
};

export const MCP_TOOL_CATALOG: McpToolCatalogEntry[] = ${JSON.stringify(entries, null, 2)} as McpToolCatalogEntry[];

export const MCP_CURATED_TOOL_COUNT = MCP_TOOL_CATALOG.filter((t) => t.curated).length;
export const MCP_FULL_TOOL_COUNT = MCP_TOOL_CATALOG.length;
`;

mkdirSync(path.dirname(outPath), { recursive: true });
writeFileSync(outPath, fileBody, 'utf8');
console.log(`Wrote ${fullCount} MCP tools (${curatedCount} curated) → ${outPath}`);

/** Escape pipe chars in markdown table cells */
function mdCell(value) {
  return String(value || '')
    .replace(/\|/g, '\\|')
    .replace(/\n/g, ' ')
    .trim();
}

function buildCuratedTableMarkdown() {
  const lines = [
    `| Tool name | toolId | Pillar | Role |`,
    `|-----------|--------|--------|------|`,
  ];
  for (const e of curated) {
    lines.push(
      `| \`${e.toolName}\` | \`${e.toolId}\` | ${e.pillar} | ${mdCell(e.description || e.name)} |`,
    );
  }
  lines.push('');
  lines.push(
    `Escape hatch (always registered): **\`syra_call_tool\`** with \`{ toolId, params }\` for any catalog tool.`,
  );
  lines.push('');
  lines.push(
    `*Auto-generated by \`scripts/sync-mcp-tools.mjs\` — ${curatedCount} curated / ${fullCount} total. Do not edit by hand.*`,
  );
  return lines.join('\n');
}

const curatedTable = buildCuratedTableMarkdown();
mkdirSync(path.dirname(curatedMdPath), { recursive: true });
writeFileSync(
  curatedMdPath,
  `# Curated MCP tools (@syra-ai/mcp-server)\n\n${curatedTable}\n`,
  'utf8',
);
console.log(`Wrote curated tools markdown → ${curatedMdPath}`);

function patchMarkerFile(filePath) {
  if (!existsSync(filePath)) {
    console.warn(`Skip marker patch (missing): ${filePath}`);
    return;
  }
  const original = readFileSync(filePath, 'utf8');
  const begin = original.indexOf(BEGIN_MARKER);
  const end = original.indexOf(END_MARKER);
  if (begin === -1 || end === -1 || end < begin) {
    console.warn(`Skip marker patch (no markers): ${filePath}`);
    return;
  }
  const next =
    original.slice(0, begin + BEGIN_MARKER.length) +
    '\n' +
    curatedTable +
    '\n' +
    original.slice(end);
  if (next !== original) {
    writeFileSync(filePath, next, 'utf8');
    console.log(`Patched curated tools table → ${filePath}`);
  } else {
    console.log(`Curated tools table already up to date → ${filePath}`);
  }
}

for (const target of MARKER_TARGETS) {
  patchMarkerFile(target);
}
