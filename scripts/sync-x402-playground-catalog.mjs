/**
 * Sync web playground x402 catalog from API discovery list + OpenAPI.
 *
 * Source of truth:
 * - api/config/x402DiscoveryResourcePaths.js
 * - api/config/x402ResourceCatalog.js
 * - openapi.json (run `npm run openapi` from api/ first)
 *
 * Run: node scripts/sync-x402-playground-catalog.mjs
 */
import { readFileSync, writeFileSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');

const { X402_DISCOVERY_RESOURCE_PATHS } = await import(
  '../api/config/x402DiscoveryResourcePaths.js'
);
const { getResourceMeta, getResourceName, getResourceSummary } = await import(
  '../api/config/x402ResourceCatalog.js'
);

const openapiPath = path.join(repoRoot, 'openapi.json');
const openapi = JSON.parse(readFileSync(openapiPath, 'utf8'));

const SELECTOR_PARAM_KEYS = new Set([
  'segment',
  'view',
  'period',
  'ticker',
  'token',
  'symbol',
  'limit',
  'topN',
  'ref',
  'mint',
  'question',
  'list',
  'query',
  'assetClass',
  'sort',
  'order',
]);

function formatDefault(value, type) {
  if (value === undefined || value === null) return '';
  if (type === 'boolean') return String(value);
  if (typeof value === 'number') return String(value);
  if (typeof value === 'object') return JSON.stringify(value);
  return String(value);
}

function isParamEnabled(param, allQueryParams) {
  if (param.required) return true;
  if (allQueryParams.length === 1) return true;
  if (param.schema?.default !== undefined && SELECTOR_PARAM_KEYS.has(param.name)) {
    return true;
  }
  return false;
}

function openApiQueryParams(openApiPath) {
  const pathItem = openapi.paths?.[openApiPath];
  if (!pathItem || typeof pathItem !== 'object') return [];
  const op = pathItem.get ?? pathItem.post;
  const queryParams = (op?.parameters ?? []).filter((p) => p.in === 'query');
  if (!queryParams.length) return [];
  return queryParams.map((p) => ({
    key: p.name,
    value: formatDefault(p.schema?.default, p.schema?.type),
    enabled: isParamEnabled(p, queryParams),
    description: p.description ?? p.schema?.description ?? '',
  }));
}

function pickMethod(openApiPath, meta) {
  const pathItem = openapi.paths?.[openApiPath];
  if (pathItem?.get) return 'GET';
  if (pathItem?.post) return 'POST';
  return meta?.methods?.[0] ?? 'GET';
}

function buildLabel(segment) {
  const meta = getResourceMeta(segment);
  const name = getResourceName(segment);
  const summary = getResourceSummary(segment);
  if (summary && summary !== name) return `${name}: ${summary}`;
  return name;
}

function segmentToId(segment) {
  return `x402-${segment.replace(/\//g, '-')}`;
}

function truncateText(text, max = 140) {
  if (!text || text.length <= max) return text;
  return `${text.slice(0, max - 1).trim()}…`;
}

function openApiPriceUsd(openApiPath, meta) {
  const pathItem = openapi.paths?.[openApiPath];
  const op = pathItem?.get ?? pathItem?.post;
  const amount = op?.['x-payment-info']?.price?.amount;
  if (amount != null && amount !== '') return String(amount);
  if (meta?.suggestedPriceStx != null) return String(meta.suggestedPriceStx);
  return undefined;
}

function buildCatalogMeta(segment, openApiPath, meta) {
  const pathItem = openapi.paths?.[openApiPath];
  const op = pathItem?.get ?? pathItem?.post;
  return {
    segment,
    name: getResourceName(segment),
    summary: getResourceSummary(segment),
    description: truncateText(meta?.description ?? op?.description),
    priceUsd: openApiPriceUsd(openApiPath, meta),
    category: meta?.category,
  };
}

const resources = X402_DISCOVERY_RESOURCE_PATHS.map((segment) => {
  const openApiPath = `/${segment}`;
  const meta = getResourceMeta(segment);
  return {
    segment,
    id: segmentToId(segment),
    label: buildLabel(segment),
    method: pickMethod(openApiPath, meta),
    params: openApiQueryParams(openApiPath),
    catalogMeta: buildCatalogMeta(segment, openApiPath, meta),
  };
});

const pathsOut = path.join(repoRoot, 'web/src/lib/x402DiscoveryResourcePaths.ts');
writeFileSync(
  pathsOut,
  `/**
 * Resource path segments for GET /.well-known/x402 (no leading slash).
 * AUTO-GENERATED by scripts/sync-x402-playground-catalog.mjs — do not edit manually.
 * Source: api/config/x402DiscoveryResourcePaths.js
 */
export const X402_DISCOVERY_RESOURCE_PATHS = ${JSON.stringify(X402_DISCOVERY_RESOURCE_PATHS, null, 2)} as const;

export type X402DiscoveryResourcePath = (typeof X402_DISCOVERY_RESOURCE_PATHS)[number];

export function normalizeSyraApiPath(pathname: string): string {
  let p = pathname.trim().toLowerCase();
  if (!p) return "/";
  if (!p.startsWith("/")) p = \`/\${p}\`;
  return p.replace(/\\/+$/, "") || "/";
}

/** True when pathname is a Syra resource listed in GET /.well-known/x402. */
export function isSyraX402DiscoveryPath(pathname: string): boolean {
  const p = normalizeSyraApiPath(pathname);
  return X402_DISCOVERY_RESOURCE_PATHS.some((segment) => {
    const base = \`/\${segment}\`;
    return p === base || p.startsWith(\`\${base}/\`);
  });
}
`,
  'utf8',
);

const catalogOut = path.join(repoRoot, 'web/src/lib/x402PlaygroundCatalog.generated.ts');
writeFileSync(
  catalogOut,
  `/**
 * Syra x402 playground catalog (offline fallback + tests).
 * AUTO-GENERATED by scripts/sync-x402-playground-catalog.mjs — do not edit manually.
 */
import type { HttpMethod } from "@/types/api";
import type { RequestParam } from "@/types/api";

export const X402_PLAYGROUND_GENERATED_AT = ${JSON.stringify(new Date().toISOString())};

export interface X402PlaygroundResourceTemplate {
  segment: string;
  id: string;
  label: string;
  method: HttpMethod;
  params: RequestParam[];
  body?: string;
  catalogMeta?: {
    segment: string;
    name: string;
    summary: string;
    description?: string;
    priceUsd?: string;
    category?: string;
  };
}

export const X402_PLAYGROUND_RESOURCE_TEMPLATES: X402PlaygroundResourceTemplate[] = ${JSON.stringify(resources, null, 2)};
`,
  'utf8',
);

console.log(
  `Synced ${resources.length} x402 playground resources → web/src/lib/x402DiscoveryResourcePaths.ts, web/src/lib/x402PlaygroundCatalog.generated.ts`,
);
