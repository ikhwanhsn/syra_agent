/**
 * Sync documentation agent tools catalog from api/config/agentTools.js.
 *
 * Preserves handcrafted example prompts from agentToolsExamplePrompts.json
 * (seed from existing catalog on first run).
 *
 * Run: node scripts/sync-agent-tools-docs.mjs
 */
import { readFileSync, writeFileSync, existsSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');

const { AGENT_TOOLS } = await import('../api/config/agentTools.js');

const promptsPath = path.join(repoRoot, 'documentation/src/data/agentToolsExamplePrompts.json');
const catalogPath = path.join(repoRoot, 'documentation/src/data/agentToolsCatalog.ts');

/** @type {Record<string, string>} */
let examplePromptOverrides = {};
if (existsSync(promptsPath)) {
  examplePromptOverrides = JSON.parse(readFileSync(promptsPath, 'utf8'));
} else if (existsSync(catalogPath)) {
  const src = readFileSync(catalogPath, 'utf8');
  const blockRe = /\{\s*id:\s*"([^"]+)"[\s\S]*?examplePrompt:\s*"((?:\\.|[^"\\])*)"/g;
  let m;
  while ((m = blockRe.exec(src))) {
    examplePromptOverrides[m[1]] = m[2];
  }
  writeFileSync(promptsPath, JSON.stringify(examplePromptOverrides, null, 2) + '\n');
  console.log(`Seeded ${Object.keys(examplePromptOverrides).length} example prompts → agentToolsExamplePrompts.json`);
}

/**
 * @param {{ id: string; stablecryptoPath?: string; stablesocialPath?: string; stableenrichPath?: string; nansenPath?: string; zerionPath?: string; birdeyePath?: string; path: string }} tool
 * @returns {'core' | 'partner' | 'memecoin' | 'market-data' | 'social-data' | 'enrichment-data'}
 */
function categoryFor(tool) {
  const id = tool.id;
  if (id.startsWith('stablecrypto-') || tool.stablecryptoPath) return 'market-data';
  if (id.startsWith('stablesocial-') || tool.stablesocialPath) return 'social-data';
  if (id.startsWith('stableenrich-') || tool.stableenrichPath) return 'enrichment-data';
  if (
    id.startsWith('gmgn-') ||
    id.startsWith('zerion-') ||
    id.includes('nansen') ||
    id.startsWith('squid-') ||
    id.startsWith('giza-') ||
    id.startsWith('bankr-') ||
    id.startsWith('neynar-') ||
    id.startsWith('siwa-') ||
    id.startsWith('birdeye-') ||
    id.startsWith('purch-') ||
    id.startsWith('heylol') ||
    id.startsWith('agentscore-') ||
    id.startsWith('paysh-') ||
    id.startsWith('quicknode-') ||
    id.startsWith('8004scan-') ||
    tool.nansenPath ||
    tool.zerionPath ||
    tool.birdeyePath ||
    tool.purchVaultPath
  ) {
    return 'partner';
  }
  if (
    id.includes('pumpfun') ||
    id.includes('pump.fun') ||
    id === 'coingecko-scout' ||
    id === 'rise-scout' ||
    id.includes('rugcheck') ||
    id.includes('dexscreener') ||
    id.includes('memecoin')
  ) {
    return 'memecoin';
  }
  return 'core';
}

/**
 * @param {string} s
 */
function escapeTsString(s) {
  return s.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

/**
 * @param {{ id: string; name: string; description: string }} tool
 */
function defaultExamplePrompt(tool) {
  const short = tool.description.split(/[.;]/)[0]?.trim() || tool.name;
  return `Use ${tool.name}: ${short}`;
}

const entries = AGENT_TOOLS.map((tool) => {
  const priceUsd = tool.displayPriceUsd ?? tool.priceUsd;
  const examplePrompt = examplePromptOverrides[tool.id] ?? defaultExamplePrompt(tool);
  if (!examplePromptOverrides[tool.id]) {
    examplePromptOverrides[tool.id] = examplePrompt;
  }
  return {
    id: tool.id,
    name: tool.name,
    description: tool.description,
    priceUsd,
    examplePrompt,
    category: categoryFor(tool),
  };
});

writeFileSync(promptsPath, JSON.stringify(examplePromptOverrides, null, 2) + '\n');

const categoryOrder = ['core', 'market-data', 'social-data', 'enrichment-data', 'partner', 'memecoin'];
const categoryLabels = {
  core: 'Core',
  'market-data': 'Market data (StableCrypto)',
  'social-data': 'Social data (StableSocial)',
  'enrichment-data': 'Enrichment (StableEnrich)',
  partner: 'Partner',
  memecoin: 'Memecoin & scouts',
};

const lines = [
  '/**',
  ' * Syra Agent tools catalog for documentation.',
  ' * AUTO-GENERATED by scripts/sync-agent-tools-docs.mjs — do not edit entries by hand.',
  ' * Example prompts: documentation/src/data/agentToolsExamplePrompts.json',
  ' */',
  '',
  'export interface AgentToolCatalogEntry {',
  '  id: string;',
  '  name: string;',
  '  description: string;',
  '  priceUsd: number;',
  '  examplePrompt: string;',
  '  category: "core" | "partner" | "memecoin" | "market-data" | "social-data" | "enrichment-data";',
  '}',
  '',
  'export const AGENT_TOOLS_CATALOG: AgentToolCatalogEntry[] = [',
];

for (const cat of categoryOrder) {
  const group = entries.filter((e) => e.category === cat);
  if (!group.length) continue;
  lines.push(`  // ${categoryLabels[cat]}`);
  for (const e of group) {
    lines.push('  {');
    lines.push(`    id: "${escapeTsString(e.id)}",`);
    lines.push(`    name: "${escapeTsString(e.name)}",`);
    lines.push(`    description: "${escapeTsString(e.description)}",`);
    lines.push(`    priceUsd: ${e.priceUsd},`);
    lines.push(`    examplePrompt: "${escapeTsString(e.examplePrompt)}",`);
    lines.push(`    category: "${e.category}",`);
    lines.push('  },');
  }
  lines.push('');
}

lines.push('];');
lines.push('');
lines.push('function formatPrice(usd: number): string {');
lines.push('  if (usd < 0.001) return "< $0.001";');
lines.push('  if (usd < 0.01) return `$${usd.toFixed(4)}`;');
lines.push('  return `$${usd.toFixed(2)}`;');
lines.push('}');
lines.push('');
lines.push('export function getToolsByCategory() {');
lines.push('  const core = AGENT_TOOLS_CATALOG.filter((t) => t.category === "core");');
lines.push('  const marketData = AGENT_TOOLS_CATALOG.filter((t) => t.category === "market-data");');
lines.push('  const socialData = AGENT_TOOLS_CATALOG.filter((t) => t.category === "social-data");');
lines.push('  const enrichmentData = AGENT_TOOLS_CATALOG.filter((t) => t.category === "enrichment-data");');
lines.push('  const partner = AGENT_TOOLS_CATALOG.filter((t) => t.category === "partner");');
lines.push('  const memecoin = AGENT_TOOLS_CATALOG.filter((t) => t.category === "memecoin");');
lines.push('  return { core, marketData, socialData, enrichmentData, partner, memecoin };');
lines.push('}');
lines.push('');
lines.push('export { formatPrice };');
lines.push('');

writeFileSync(catalogPath, lines.join('\n'));
console.log(`Synced ${entries.length} agent tools → documentation/src/data/agentToolsCatalog.ts`);
